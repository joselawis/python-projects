# Strings
print("Hello World")
print('Hello World')
print("""Hello World""")
print('''Hello World''')
print(type('Hello world'))

# Integer
print(10)
print(type(10))

# Float
print(30.1)
print(type(30.1))

# Boolean
print(True)
print(False)
print(type(True))

# List
print([1, 2, 3, 4])
print(['Hello', 'Bye'])
print([10, 'Hello', True, 23.9])
print(type([]))

# Tuples (not modifiable)
print((10, 20, 30))
print(type(()))

# Dictionaries
dictName = {"name": "Ryan", "surname": "Ray", "alias": "Fazt", "age": 30}
print(dictName)
print(type(dictName))
